    public void readJTable() throws SQLException {
        DefaultTableModel modelo = (DefaultTableModel) tbMusica.getModel();
        modelo.setNumRows(0);
        MusicasDao mdao = new MusicasDao();
        for (Musicas m : mdao.read()) {
            modelo.addRow(new Object[]{
                m.getIdMusica(),
                m.getNomeMusica(),
                m.getArtista(),
                m.getAlbum(),
                m.getGenero(),
                m.getAno(),
                m.getProdutora(),
                m.getDuracao()
            });
        }
    }

