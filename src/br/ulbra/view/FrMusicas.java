    private void btPesquisarrActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btPesquisarrActionPerformed
        try {
            int tipo = 0;
            switch (cbTipoMusica.getSelectedIndex()) {
                case 0:
                    tipo = 0;
                    break;
                case 1:
                    tipo = 1;
            }
            
            if (tipo == 0 ) {
                listarNomeMusica(edPesquisaMusica.getText(), tipo);

            } else  {
                listarArtista(edPesquisaMusica.getText(), tipo);
        
        }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro:" + ex.getMessage());
        }
    }//GEN-LAST:event_btPesquisarrActionPerformed

    public void listarNomeMusica(String nomeMusica, int tipo) throws SQLException {
        DefaultTableModel modelo
                = (DefaultTableModel) tbMusica.getModel();
        modelo.setNumRows(0);
        MusicasDao mdao = new MusicasDao();
        for (Musicas m : mdao.listarTNomeMusica(nomeMusica, tipo)) {
            modelo.addRow(new Object[]{
                m.getIdMusica(),
                m.getNomeMusica(),
                m.getArtista(),
                m.getAlbum(),
                m.getGenero(),
                m.getAno(),
                m.getProdutora(),
                m.getDuracao()
            });
        }
    }
